	<h1>Úvodem</h1>
	<p>Vážení přátelé filmového umění, <br />
    srdečně vás vítáme na Filmovém festivalu Fakulty informatiky, kde letos
    již popatnácté studenti MU představí svou původní filmovou tvorbu. Pod
    záštitou děkana FI MU prof. RNDr. Michala Kozubka, Ph.D. a primátora
    města Brna Ing.&nbsp;Petra Vokřála proběhne promítání původních
    autorských filmů dne 15. 5. 2015 večer, opět na Fakultě informatiky MU. </p>

    <p>Letošní ročník má motto <em>střih!</em></p>

    <p>Uvidíte výsledek snahy studentů MU o vyjádření se filmovou řečí,
    kdy desítku profesí, prakticky vše od námětu až po produkci, si dělají
    malé týmy studenti svépomocí, pod mentorstvím pedagogů. I letos se do
    přípravy se zapojily týmy studentů několika fakult MU a také
    studenti herectví JAMU.</p>

    <p>Doufáme, že si každý z nabízeného menu vybere alespoň některý snímek,
    který navrhne na ocenění v divácké anketě, jejíž vyhlášení proběhne na
    závěr festivalu. Zástupci sponzorů a odborné poroty předají věcné ceny
    sponzorů.</p>
	<p class="podpis">Petr Sojka, Robert Král, Radovan Hakl, Josef Víšek</p>

