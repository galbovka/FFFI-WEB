    <h1>Introduction</h1>
    <p>Dear friends of the film art, <br />
    it's our pleasure to welcome you to the Film Festival of Faculty of Informatics, event where the students and graduates of MU present their own film work.
    Projection of auctorial movies will be held 15. 5. 2015 in the evening at the faculty
    under the patronage of the faculty dean prof. RNDr. Michal Kozubek, Ph.D. and Brno chief magistrate Ing. Petr Vokřál.</p>

    <p>The motto of this year's festival is <em>cut!</em></p>

    <p>You'll see the fruit of students endeavor to express themselves in short movies,
    where dozens of professions, practically everything from motive to production, are executed by small group of students under the mentorships of their teachers.
    Students from various faculties plus acting students from JAMU engaged in preparation of the festival.</p>

    <p>We hope that everyone will choose at least one picture to recommend for a reward in the viewer's poll, 
    the results of which will be announced at the end of the festival. Prices from our sponsor will be handed out by the representatives of our sponsors and jury.</p>
    <p class="podpis">Petr Sojka, Robert Král, Radovan Hakl, Josef Víšek</p>
    
